void clearScreen() {
    // Defining how to clear a screen
    system("cls");
}

void printDigit(int digit) {
    // Checks for a digit, then prints it line by line.
    // If the digit is 10, we combine 1 and 0.
    if (digit >= 0 && digit <= 9) {
        for (const std::string& line : DIGITS[digit]) {
            std::cout << line << std::endl;
        }
    } else if (digit = 10) {
        for (size_t i = 0; i < DIGITS[1].size(); ++i) {
        // Print line 'i' of digit 1, then a space, then line 'i' of digit 0.
            std::cout << DIGITS[1][i] << "  " << DIGITS[0][i] << std::endl;
        }
    }

}

int main () {
    // A countdown loop.
    for (int i = 10; i >= 0; i--) {
        clearScreen();

        std::cout << "\n";

        printDigit(i);

        std::cout << "\n";

        // Print out the status.
        if (i > 7) std::cout << "Santa is preparing..." << std::endl;
        else if (i > 4) std::cout << "Reindeer are buckling up..." << std::endl;
        else if (i > 0) std::cout << "Sleigh warming up..." << std::endl;
        else std::cout << "SANTA'S SLEIGH IS LAUNCHING!" << std::endl;

        std::this_thread::sleep_for(std::chrono::seconds(1)); // Wait for 1 second.
    }

    return 0;
}
